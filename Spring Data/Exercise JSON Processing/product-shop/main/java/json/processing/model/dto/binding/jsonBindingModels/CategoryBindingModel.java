package json.processing.model.dto.binding.jsonBindingModels;

public class CategoryBindingModel {

    private Long id;
    private String name;

    public CategoryBindingModel() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
